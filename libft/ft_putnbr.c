#include "libft.h"

static void		q(int nb)
{
	char	a;

	if (!nb)
		return ;
	q(nb / 10);
	a = nb % 10 + '0';
	write(1, &a, 1);
}

void		ft_putnbr(int nb)
{
	if (nb == 0 || nb == -2147483648)
	{
		write(1, (nb == 0) ? "0" : "-2147483648", (nb == 0) ? 1 : 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	q(nb);
}
